/**
 * Procedurally generates the 81-month Mall clothing store sales dataset.
 * It contains realistic monthly trends, veteran sales, late low-performing hires,
 * and specific problem anomalies (Month 1 ramp-up, Month 74-75 drop, Month 76 surge with 8 staff).
 */
export function getRawDataset(): SalesRecord[] {
  const records: SalesRecord[] = [];
  const activePools: { [key: number]: string[] } = {};

  // Standard overlaps
  const overlaps = [15, 28, 40, 45, 52, 60, 68, 76, 80];

  for (let m = 1; m <= 81; m++) {
    let count = 7;
    let active: string[] = ["sp1", "sp2", "sp4", "sp6", "sp7"]; // Core 5 veterans always there

    if (m === 1) {
      count = 3;
      active = ["sp1", "sp2", "sp3"];
    } else if (m === 2) {
      count = 5;
      active = ["sp1", "sp2", "sp3", "sp4", "sp5"];
    } else if (m === 3) {
      count = 7;
      active = ["sp1", "sp2", "sp3", "sp4", "sp5", "sp6", "sp7"];
    } else {
      if (overlaps.includes(m)) {
        count = 8;
      }

      // sp3 left in month 44, replaced by sp8 at month 45
      if (m < 45) {
        active.push("sp3");
      } else {
        active.push("sp8");
      }

      // sp5 left in month 60, replaced by sp9 at month 61
      if (m <= 60) {
        active.push("sp5");
      } else {
        active.push("sp9");
      }

      // Addition of temporary overlaps
      if (count === 8) {
        if (m === 15) active.push("sp10");
        else if (m === 28) active.push("sp11");
        else if (m === 40) active.push("sp12");
        else if (m === 45) active.push("sp3"); // Brief transition overlap
        else if (m === 52) active.push("sp13");
        else if (m === 60) active.push("sp5");  // Brief transition overlap
        else if (m === 68) active.push("sp14");
        else if (m === 76) active.push("sp15");
        else if (m === 80) active.push("sp16");
      }
    }

    activePools[m] = active;

    // Seasonality cycle (peaks in late autum/winter, index 10-12)
    const monthMod = m % 12;
    const seasonMult = 1.0 + 0.25 * Math.sin((2 * Math.PI * (monthMod - 8)) / 12);

    const row: SalesRecord = {
      Month: m,
      Staff_Count: count,
      Total_Sales: 0,
    };

    // Initialize all 16 salespersons
    for (let s = 1; s <= 16; s++) {
      row[`sp${s}`] = 0;
    }

    // Explicit Month 1 values (Ramp up)
    if (m === 1) {
      row["sp1"] = 9.5;
      row["sp2"] = 8.2;
      row["sp3"] = 5.3;
      row["Total_Sales"] = 23.0;
    } 
    // Explicit Month 74 values (sales dip to 96k)
    else if (m === 74) {
      row["sp1"] = 16.1;
      row["sp2"] = 15.8;
      row["sp4"] = 17.2;
      row["sp6"] = 15.0;
      row["sp7"] = 14.7;
      row["sp8"] = 13.9;
      row["sp9"] = 3.3; // sp9 is underperforming new hire
      row["Total_Sales"] = 96.0;
    } 
    // Explicit Month 75 values (sales dip to 61k)
    else if (m === 75) {
      row["sp1"] = 11.0;
      row["sp2"] = 10.5;
      row["sp4"] = 11.5;
      row["sp6"] = 9.8;
      row["sp7"] = 9.2;
      row["sp8"] = 8.0;
      row["sp9"] = 1.0; // very low month
      row["Total_Sales"] = 61.0;
    } 
    // Explicit Month 76 values (surge to 130k with 8 staff overlap)
    else if (m === 76) {
      row["sp1"] = 21.0;
      row["sp2"] = 20.4;
      row["sp4"] = 22.0;
      row["sp6"] = 19.5;
      row["sp7"] = 19.0;
      row["sp8"] = 18.2;
      row["sp9"] = 6.5;
      row["sp15"] = 3.4; // sp15 weak helper
      row["Total_Sales"] = 130.0;
    } 
    // Regular month generation
    else {
      let sumOfSellers = 0;
      active.forEach((seller) => {
        let base = 11.0;
        let std = 0.8;

        if (seller === "sp1") { base = 13.4; std = 0.5; }
        else if (seller === "sp2") { base = 13.2; std = 0.5; }
        else if (seller === "sp4") { base = 14.0; std = 0.6; }
        else if (seller === "sp3") { base = 12.5; std = 0.5; }
        else if (seller === "sp6") { base = 11.5; std = 0.5; }
        else if (seller === "sp5") { base = 11.8; std = 0.4; }
        else if (seller === "sp7") { base = 10.8; std = 0.4; }
        else if (seller === "sp8") { base = 10.5; std = 0.4; }
        else if (seller === "sp9") { base = 9.0; std = 0.5; }
        else if (["sp10", "sp11", "sp12"].includes(seller)) { base = 7.5; std = 0.6; }
        else { // Late low perf (sp13, 14, 15, 16) - well below 5k
          base = seller === "sp13" ? 3.2 : seller === "sp14" ? 3.5 : seller === "sp15" ? 3.1 : 2.8;
          std = 0.3;
        }

        // Apply pseudo-random distribution around the seed for that month
        const sellerNum = parseInt(seller.replace("sp", "")) || 1;
        const seedValue = Math.sin(m * sellerNum * 13.57) * std;
        const val = Math.max(0, parseFloat((base * seasonMult + seedValue).toFixed(1)));
        row[seller] = val;
        sumOfSellers += val;
      });

      row["Total_Sales"] = parseFloat(sumOfSellers.toFixed(1));
    }

    records.push(row);
  }

  // Inject intentional validation defects for the Data Validation explorer
  // Anomalies:
  // 1. Negative sales value in month 35, sp10 is -5.5 (typo)
  records[34]["sp10"] = -5.5; 
  // 2. Missing cell in Month 12 representation (sp5 has NaN/undefined, simulated here as null)
  // We'll set this property to represent as unparsed/nan
  (records[11] as any)["sp5"] = null;

  // 3. Mathematical outlier in Month 50: sp1 has a ₹134k typo (13.4k became 134k)
  records[49]["sp1"] = 134.0;
  records[49]["Total_Sales"] = parseFloat(Object.keys(records[49])
    .filter(k => k.startsWith("sp"))
    .map(k => records[49][k] || 0)
    .reduce((sum, curr) => sum + curr, 0)
    .toFixed(1));

  return records;
}

/**
 * Executes a TypeScript port of the data cleaning pipeline:
 * - Replaces negative sales with 0.0
 * - Replaces NaN/null elements with 0.0
 * - Resolves total store summation discrepancies
 */
export function cleanDataset(raw: SalesRecord[]): SalesRecord[] {
  return raw.map((orig) => {
    const row = { ...orig };
    let rowSum = 0;

    for (let s = 1; s <= 16; s++) {
      const key = `sp${s}`;
      let val = row[key];

      // Convert from string if necessary
      if (typeof val === "string") {
        val = parseFloat(val);
      }

      // Clean empty/null/NaN
      if (val === null || val === undefined || typeof val !== "number" || isNaN(val)) {
        val = 0.0;
      }
      // Clean negative
      if (val < 0) {
        val = 0.0;
      }

      row[key] = val;
      rowSum += val;
    }

    row["Total_Sales"] = parseFloat(rowSum.toFixed(1));
    return row;
  });
}

